export const buildMarginClassNames = (props?: Margin): string => {
  if (!props) return "m-0";
  const result: string[] = [];
  if (props.margin) {
    if (props.margin?.xs) result.push(MarginStyleCssMap["xs"][props.margin.xs]);
    if (props.margin?.sm) result.push(MarginStyleCssMap["sm"][props.margin.sm]);
    if (props.margin?.md) result.push(MarginStyleCssMap["md"][props.margin.md]);
    if (props.margin?.lg) result.push(MarginStyleCssMap["lg"][props.margin.lg]);
  }

  if (props.marginX) {
    if (props.marginX?.xs)
      result.push(MarginXStyleCssMap["xs"][props.marginX.xs]);
    if (props.marginX?.sm)
      result.push(MarginXStyleCssMap["sm"][props.marginX.sm]);
    if (props.marginX?.md)
      result.push(MarginXStyleCssMap["md"][props.marginX.md]);
    if (props.marginX?.lg)
      result.push(MarginXStyleCssMap["lg"][props.marginX.lg]);
  }

  if (props.marginY) {
    if (props.marginY?.xs)
      result.push(MarginYStyleCssMap["xs"][props.marginY.xs]);
    if (props.marginY?.sm)
      result.push(MarginYStyleCssMap["sm"][props.marginY.sm]);
    if (props.marginY?.md)
      result.push(MarginYStyleCssMap["md"][props.marginY.md]);
    if (props.marginY?.lg)
      result.push(MarginYStyleCssMap["lg"][props.marginY.lg]);
  }

  if (props.marginLeft) {
    if (props.marginLeft?.xs)
      result.push(MarginLeftStyleCssMap["xs"][props.marginLeft.xs]);
    if (props.marginLeft?.sm)
      result.push(MarginLeftStyleCssMap["sm"][props.marginLeft.sm]);
    if (props.marginLeft?.md)
      result.push(MarginLeftStyleCssMap["md"][props.marginLeft.md]);
    if (props.marginLeft?.lg)
      result.push(MarginLeftStyleCssMap["lg"][props.marginLeft.lg]);
  }

  if (props.marginRight) {
    if (props.marginRight?.xs)
      result.push(MarginRightStyleCssMap["xs"][props.marginRight.xs]);
    if (props.marginRight?.sm)
      result.push(MarginRightStyleCssMap["sm"][props.marginRight.sm]);
    if (props.marginRight?.md)
      result.push(MarginRightStyleCssMap["md"][props.marginRight.md]);
    if (props.marginRight?.lg)
      result.push(MarginRightStyleCssMap["lg"][props.marginRight.lg]);
  }

  if (props.marginTop) {
    if (props.marginTop?.xs)
      result.push(MarginTopStyleCssMap["xs"][props.marginTop.xs]);
    if (props.marginTop?.sm)
      result.push(MarginTopStyleCssMap["sm"][props.marginTop.sm]);
    if (props.marginTop?.md)
      result.push(MarginTopStyleCssMap["md"][props.marginTop.md]);
    if (props.marginTop?.lg)
      result.push(MarginTopStyleCssMap["lg"][props.marginTop.lg]);
  }

  if (props.marginBottom) {
    if (props.marginBottom?.xs)
      result.push(MarginBottomStyleCssMap["xs"][props.marginBottom.xs]);
    if (props.marginBottom?.sm)
      result.push(MarginBottomStyleCssMap["sm"][props.marginBottom.sm]);
    if (props.marginBottom?.md)
      result.push(MarginBottomStyleCssMap["md"][props.marginBottom.md]);
    if (props.marginBottom?.lg)
      result.push(MarginBottomStyleCssMap["lg"][props.marginBottom.lg]);
  }

  return result.join(" ");
};
