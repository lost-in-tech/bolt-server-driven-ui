export const buildBorderClassNames = (props?: Border): string => {
  if (!props) return "border-0";
  const result: string[] = [];

  if (props.borderWidth) {
    if (props.borderWidth?.xs)
      result.push(BorderWidthCssMap["xs"][props.borderWidth.xs]);
    if (props.borderWidth?.sm)
      result.push(BorderWidthCssMap["sm"][props.borderWidth.sm]);
    if (props.borderWidth?.md)
      result.push(BorderWidthCssMap["md"][props.borderWidth.md]);
    if (props.borderWidth?.lg)
      result.push(BorderWidthCssMap["lg"][props.borderWidth.lg]);
  }

  if (props.borderWidthX) {
    if (props.borderWidthX?.xs)
      result.push(BorderWidthXCssMap["xs"][props.borderWidthX.xs]);
    if (props.borderWidthX?.sm)
      result.push(BorderWidthXCssMap["sm"][props.borderWidthX.sm]);
    if (props.borderWidthX?.md)
      result.push(BorderWidthXCssMap["md"][props.borderWidthX.md]);
    if (props.borderWidthX?.lg)
      result.push(BorderWidthXCssMap["lg"][props.borderWidthX.lg]);
  }

  if (props.borderWidthY) {
    if (props.borderWidthY?.xs)
      result.push(BorderWidthYCssMap["xs"][props.borderWidthY.xs]);
    if (props.borderWidthY?.sm)
      result.push(BorderWidthYCssMap["sm"][props.borderWidthY.sm]);
    if (props.borderWidthY?.md)
      result.push(BorderWidthYCssMap["md"][props.borderWidthY.md]);
    if (props.borderWidthY?.lg)
      result.push(BorderWidthYCssMap["lg"][props.borderWidthY.lg]);
  }

  if (props.borderWidthLeft) {
    if (props.borderWidthLeft?.xs)
      result.push(BorderWidthLeftCssMap["xs"][props.borderWidthLeft.xs]);
    if (props.borderWidthLeft?.sm)
      result.push(BorderWidthLeftCssMap["sm"][props.borderWidthLeft.sm]);
    if (props.borderWidthLeft?.md)
      result.push(BorderWidthLeftCssMap["md"][props.borderWidthLeft.md]);
    if (props.borderWidthLeft?.lg)
      result.push(BorderWidthLeftCssMap["lg"][props.borderWidthLeft.lg]);
  }

  if (props.borderWidthRight) {
    if (props.borderWidthRight?.xs)
      result.push(BorderWidthRightCssMap["xs"][props.borderWidthRight.xs]);
    if (props.borderWidthRight?.sm)
      result.push(BorderWidthRightCssMap["sm"][props.borderWidthRight.sm]);
    if (props.borderWidthRight?.md)
      result.push(BorderWidthRightCssMap["md"][props.borderWidthRight.md]);
    if (props.borderWidthRight?.lg)
      result.push(BorderWidthRightCssMap["lg"][props.borderWidthRight.lg]);
  }

  if (props.borderWidthTop) {
    if (props.borderWidthTop?.xs)
      result.push(BorderWidthTopCssMap["xs"][props.borderWidthTop.xs]);
    if (props.borderWidthTop?.sm)
      result.push(BorderWidthTopCssMap["sm"][props.borderWidthTop.sm]);
    if (props.borderWidthTop?.md)
      result.push(BorderWidthTopCssMap["md"][props.borderWidthTop.md]);
    if (props.borderWidthTop?.lg)
      result.push(BorderWidthTopCssMap["lg"][props.borderWidthTop.lg]);
  }

  if (props.borderWidthBottom) {
    if (props.borderWidthBottom?.xs)
      result.push(BorderWidthBottomCssMap["xs"][props.borderWidthBottom.xs]);
    if (props.borderWidthBottom?.sm)
      result.push(BorderWidthBottomCssMap["sm"][props.borderWidthBottom.sm]);
    if (props.borderWidthBottom?.md)
      result.push(BorderWidthBottomCssMap["md"][props.borderWidthBottom.md]);
    if (props.borderWidthBottom?.lg)
      result.push(BorderWidthBottomCssMap["lg"][props.borderWidthBottom.lg]);
  }

  return result.join(" ");
};
