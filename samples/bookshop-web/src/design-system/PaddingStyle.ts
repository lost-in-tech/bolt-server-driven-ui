export const buildPaddingClassNames = (props?: Padding): string => {
  if (!props) return "";
  const result: string[] = [];
  if (props.padding) {
    if (props.padding?.xs)
      result.push(PaddingStyleCssMap["xs"][props.padding.xs]);
    if (props.padding?.sm)
      result.push(PaddingStyleCssMap["sm"][props.padding.sm]);
    if (props.padding?.md)
      result.push(PaddingStyleCssMap["md"][props.padding.md]);
    if (props.padding?.lg)
      result.push(PaddingStyleCssMap["lg"][props.padding.lg]);
  }

  if (props.paddingX) {
    if (props.paddingX?.xs)
      result.push(PaddingXStyleCssMap["xs"][props.paddingX.xs]);
    if (props.paddingX?.sm)
      result.push(PaddingXStyleCssMap["sm"][props.paddingX.sm]);
    if (props.paddingX?.md)
      result.push(PaddingXStyleCssMap["md"][props.paddingX.md]);
    if (props.paddingX?.lg)
      result.push(PaddingXStyleCssMap["lg"][props.paddingX.lg]);
  }

  if (props.paddingY) {
    if (props.paddingY?.xs)
      result.push(PaddingYStyleCssMap["xs"][props.paddingY.xs]);
    if (props.paddingY?.sm)
      result.push(PaddingYStyleCssMap["sm"][props.paddingY.sm]);
    if (props.paddingY?.md)
      result.push(PaddingYStyleCssMap["md"][props.paddingY.md]);
    if (props.paddingY?.lg)
      result.push(PaddingYStyleCssMap["lg"][props.paddingY.lg]);
  }

  if (props.paddingLeft) {
    if (props.paddingLeft?.xs)
      result.push(PaddingLeftStyleCssMap["xs"][props.paddingLeft.xs]);
    if (props.paddingLeft?.sm)
      result.push(PaddingLeftStyleCssMap["sm"][props.paddingLeft.sm]);
    if (props.paddingLeft?.md)
      result.push(PaddingLeftStyleCssMap["md"][props.paddingLeft.md]);
    if (props.paddingLeft?.lg)
      result.push(PaddingLeftStyleCssMap["lg"][props.paddingLeft.lg]);
  }

  if (props.paddingRight) {
    if (props.paddingRight?.xs)
      result.push(PaddingRightStyleCssMap["xs"][props.paddingRight.xs]);
    if (props.paddingRight?.sm)
      result.push(PaddingRightStyleCssMap["sm"][props.paddingRight.sm]);
    if (props.paddingRight?.md)
      result.push(PaddingRightStyleCssMap["md"][props.paddingRight.md]);
    if (props.paddingRight?.lg)
      result.push(PaddingRightStyleCssMap["lg"][props.paddingRight.lg]);
  }

  if (props.paddingTop) {
    if (props.paddingTop?.xs)
      result.push(PaddingTopStyleCssMap["xs"][props.paddingTop.xs]);
    if (props.paddingTop?.sm)
      result.push(PaddingTopStyleCssMap["sm"][props.paddingTop.sm]);
    if (props.paddingTop?.md)
      result.push(PaddingTopStyleCssMap["md"][props.paddingTop.md]);
    if (props.paddingTop?.lg)
      result.push(PaddingTopStyleCssMap["lg"][props.paddingTop.lg]);
  }

  if (props.paddingBottom) {
    if (props.paddingBottom?.xs)
      result.push(PaddingBottomStyleCssMap["xs"][props.paddingBottom.xs]);
    if (props.paddingBottom?.sm)
      result.push(PaddingBottomStyleCssMap["sm"][props.paddingBottom.sm]);
    if (props.paddingBottom?.md)
      result.push(PaddingBottomStyleCssMap["md"][props.paddingBottom.md]);
    if (props.paddingBottom?.lg)
      result.push(PaddingBottomStyleCssMap["lg"][props.paddingBottom.lg]);
  }

  return result.join(" ");
};
